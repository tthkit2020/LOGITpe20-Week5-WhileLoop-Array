using System;

namespace WhileAndArray
{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 1;

            while( i != 11)
            {
                Console.WriteLine(i);
                i++;
            }

            int j = 10;
            while(j != 0)
            {
                Console.WriteLine(j);
                j--;
            }
        }
    }
}
